# âœ… App Title
st.title("ðŸ©º Diabetes Prediction using Logistic Regression")

# âœ… Sample Dataset
data = {
    'age': [50, 31, 32, 45, 29],
    'plas': [148, 85, 89, 120, 94],
    'insu': [0, 0, 94, 0, 168],
    'mass': [33.6, 26.6, 28.1, 35.3, 23.1],
    'class': [1, 0, 0, 1, 0]
}
df = pd.DataFrame(data)

# âœ… Train Logistic Regression Model
X = df[['age', 'plas', 'insu', 'mass']]
y = df['class']
model = LogisticRegression()
model.fit(X, y)

# âœ… Input Form
st.subheader("ðŸ‘¤ Enter Patient Data")
age = st.number_input("Age", min_value=1, max_value=120, value=30)
plas = st.number_input("Plasma Glucose", min_value=0, max_value=200, value=100)
insu = st.number_input("Insulin", min_value=0, max_value=1000, value=100)
mass = st.number_input("BMI", min_value=10.0, max_value=80.0, value=25.0)

# âœ… Predict Button
if st.button("Predict"):
    input_data = np.array([[age, plas, insu, mass]])
    prediction = model.predict(input_data)
    if prediction[0] == 1:
        st.success("ðŸŽ¯ Prediction: Diabetic")
    else:
        st.success("ðŸ’ª Prediction: Not Diabetic")

# âœ… Model Accuracy
st.subheader("âœ… Model Accuracy")
train_pred = model.predict(X)
accuracy = accuracy_score(y, train_pred)
st.write(f"Training Accuracy: {accuracy:.2f}")